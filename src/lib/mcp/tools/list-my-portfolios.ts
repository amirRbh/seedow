import { defineTool } from "@lovable.dev/mcp-js";
import { z } from "zod";
import { supabaseForUser } from "../supabase";

export default defineTool({
  name: "list_my_portfolios",
  title: "List my portfolios",
  description:
    "List the signed-in Seedow user's simulated portfolios (name, causes, exclusions, horizon, risk, initial amount, active status).",
  inputSchema: {},
  annotations: { readOnlyHint: true, idempotentHint: true, openWorldHint: false },
  handler: async (_input, ctx) => {
    if (!ctx.isAuthenticated()) {
      return { content: [{ type: "text", text: "Not authenticated" }], isError: true };
    }
    const supabase = supabaseForUser(ctx);
    const { data, error } = await supabase
      .from("portfolios")
      .select("id, name, causes, exclusions, horizon_years, risk_target, initial_amount, is_active, generated_at")
      .order("generated_at", { ascending: false });
    if (error) {
      return { content: [{ type: "text", text: error.message }], isError: true };
    }
    return {
      content: [{ type: "text", text: JSON.stringify(data ?? [], null, 2) }],
      structuredContent: { portfolios: data ?? [] },
    };
  },
});
